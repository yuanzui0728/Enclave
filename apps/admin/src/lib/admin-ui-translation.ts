export function translateAdminUiText(
  text: string,
  locale: SupportedLocale,
) {
  if (locale === "zh-CN" || !CJK_PATTERN.test(text)) {
    return text;
  }

  if (locale !== "en-US") {
    return text;
  }

  const leadingWhitespace = text.match(/^\s*/)?.[0] ?? "";
  const trailingWhitespace = text.match(/\s*$/)?.[0] ?? "";
  const body = text.trim().replace(/\s+/g, " ");
  if (!body) {
    return text;
  }

  const translatedBody = translateEnglishAdminTextBody(body);
  if (translatedBody === body) {
    return text;
  }

  return `${leadingWhitespace}${translatedBody}${trailingWhitespace}`;
}

function translateEnglishAdminTextBody(body: string): string {
  const exact = EN_EXACT_TRANSLATIONS.get(body);
  if (exact) {
    return exact;
  }

  const patterned = translateEnglishAdminPattern(body);
  if (patterned) {
    return patterned;
  }

  if (body.length > 72) {
    return body;
  }

  let translated = body;
  for (const [source, target] of EN_SEGMENT_TRANSLATIONS) {
    translated = translated.split(source).join(target);
  }

  translated = translated
    .replace(/（/g, " (")
    .replace(/）/g, ")")
    .replace(/：/g, ": ")
    .replace(/；/g, "; ")
    .replace(/，/g, ", ")
    .replace(/。/g, ".")
    .replace(/、/g, " / ")
    .replace(/([a-z])([A-Z])/g, "$1 $2")
    .replace(/\b(and|or|to)(?=[A-Z])/g, "$1 ")
    .replace(/([A-Za-z])\s*(and|or|to)\b/g, "$1 $2")
    .replace(/\s+/g, " ")
    .trim();

  return translated === body ? body : translated;
}

